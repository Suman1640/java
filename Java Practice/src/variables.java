public class variables {
    public static void main(String[] args) {
        // variables
        int a = 10;
        int b = 25;
        int sum = a + b;
        int diff = a - b;
        int mul = a * b;
        int Module = a % b;

        System.out.print("a+b = ");
        System.out.println(sum);
        System.out.print("a-b = ");
        System.out.println(diff);
        System.out.print("a*b = ");
        System.out.println(mul);
        System.out.print("a%b = ");
        System.out.println(Module);
    }
}
